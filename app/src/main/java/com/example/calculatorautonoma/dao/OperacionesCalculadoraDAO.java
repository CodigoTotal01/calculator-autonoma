package com.example.calculatorautonoma.dao;

public class OperacionesCalculadoraDAO {
    public int adicion(int num1 , int  num2){
        return num1 + num2;
    }

    public int substraccion(int num1 , int  num2){
        return num1 - num2;
    }

    public int multiplicacion(int num1 , int  num2){
        return num1 * num2;
    }

    public int division(int num1 , int  num2){
        return num1 / num2;
    }
}
